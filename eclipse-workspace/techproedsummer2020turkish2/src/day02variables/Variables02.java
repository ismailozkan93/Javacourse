package day02variables;

public class Variables02 {

	public static void main(String[] args) {
		int fiyat=500;
		System.out.println(fiyat);
		
		int a=5;
		int b=4;
		System.out.println(a+b);
		
		boolean burs=true;
		System.out.println(burs);
		char harf='a';
		System.out.println(harf);
		
		short nufus=23000;
		System.out.println(nufus);

		int hazine =12234567;
		System.out.println(hazine);
		
		long hazine2=126578991;
		System.out.println(hazine2);
		
		float borc=12.53f;
		System.out.println(borc);
		
		double borc2=12.53;
		System.out.println(borc2);
	}

}
