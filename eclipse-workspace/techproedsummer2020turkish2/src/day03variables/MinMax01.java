package day03variables;

public class MinMax01 {

	public static void main(String[] args) {
		// Primitive data type`larin max ve min degerlerini bulmak
		//1.int min ve max deger bul
		//2.byte min ve max degerlerini bulunuz
		//3.float min ve max degerlerini bulunuz
		//4.long min ve max degerlerini bulunuz
		//5.double min ve max degerlerini bulunuz
		
		
		
		int minValueInt=Integer.MIN_VALUE;
		System.out.println(minValueInt);
		
		int maxValueInt=Integer.MAX_VALUE;
		System.out.println(maxValueInt);

		byte minValue=Byte.MIN_VALUE;
		System.out.println(minValue);
		
		byte maxValue=Byte.MAX_VALUE;
		System.out.println(maxValue);
		
		float minValueFloat=Float.MIN_VALUE;
		System.out.println(minValueFloat);
		
		float maxValueFloat=Float.MAX_VALUE;
		System.out.println(maxValueFloat);
		
		
		
		
		
	}

}
